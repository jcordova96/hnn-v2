<h3>Who are We Looking For?</h3>

    <p>HNN is seeking dedicated interns who have a passion for history, politics, and new media. We are the only website
        around wholly devoted to the task of putting current events into historical perspective every day, but we can't
        do it without the valued contributions of our intern staff, who have the rare privilege of expanding their
        journalistic and new media skills along with their historical knowledge!</p>

    <p>We're looking for students and recent graduates who are self-starters and capable of making good judgments
        without close supervision. Excellent writing skills are mandatory; excellent writing skills for a general as
        opposed to academic audience are preferred, but not required. Experience with copyediting, copywriting,
        proofreading, Internet marketing, social media, SEO, graphic design are definite pluses, but not pre-requisites
        -- you'll be learning these skills on the job!</p>

    <p>If you have any experience with HTML coding, content management systems, or blogging, please be sure to mention
        it in your cover letter!</p>

    <h3>Basic Facts</h3>

    <p>Here are the basic facts: Internships are for a minimum term of two quarters. You should expect to put in 10+
        hours a week. <strong>INTERNS DO NOT HAVE TO WORK AT HNN'S OFFICES</strong>, though interns located in the
        Washington D.C. area are encouraged to work at HNN's branch office in Washington. Interns will communicate with
        the editor(s) through e-mail, Skype, and the telephone. Basically, if you have access to the Internet, you can
        be an intern at HNN!</p>

    <p>All internships are unpaid, but class credit can be arranged.</p>

    <p><a href="internship-responsibilities">Click here</a> to read about intern responsibilities.</p>

    <p>As you'll see, this is not a make-work job. You'll do real work; nothing Mickey Mouse.</p>

    <p>If you are interested in applying for an internship please send your resume to the editor, David Walsh. BE SURE
        TO INCLUDE THE INTERNSHIP YOU wish to apply for.</p>

    <h3>Available Positions</h3>

    <p><em>Breaking News Intern</em></p>

    <p>Breaking News interns are responsible for updating the Breaking News and Historians in the News sections of HNN
        by posting excerpts of news stories relating to history and historians. This position requires a great deal of
        Web surfing and reading news stories, good judgment over what stories are appropriate for inclusion on HNN, what
        excerpt length is appropriate, which stories should be highlighted, and whether or not stories should be
        retitled. Interns will develop headline writing skills and a familiarity with modern web design and content
        aggregation. Click here for further guidelines.</p>

    <p><em>Roundup Intern</em></p>

    <p>Roundup interns are responsible for updating the various Roundup departments on HNN, where we post excerpts from
        historically-informed commentary from around the web by both historians, other academics, journalists, and even
        politicians. This position requires a great deal of Web surfing and reading, especially of op-ed pages and
        opinion magazines, good judgment about whether an article is appropriate for excerpt, what excerpt length is
        appropriate,&nbsp; etc. Interns will develop critical reading skills and a familiarity with modern web design
        and content aggregation. Click here for further guidelines.</p>

    <p><em>Social Media Internship</em></p>

    <p>Social media interns are responsible for maintaining and improving HNN's presence on various social media
        platforms, including (but not limited to) Facebook and Twitter. Interns will be responsible for communicating
        with other Facebook, Twitter, LinkedIn, etc. groups and users to increase our brand recognition. This extends
        beyond just posting advertisements about HNN on other group walls and pages -- it means getting involved in a
        dialog with other users. Good judgment and a cheerful disposition are essential for success as a social media
        intern!</p>

    <p><em>Editorial Internship</em></p>

    <p>Editorial interns are responsible for soliciting articles from historians, working with authors to improve
        articles, copyediting, proofreading, and uploading pieces onto the HNN website. This position requires excellent
        judgment, strong writing and editing skills, and Web savvy.</p>

    <p><em>Feature Internship</em></p>

    <p>Feature interns are responsible for implementing feature projects, typically two to three per intern. These
        projects could include profiles of historians, working on the HNN Teacher's Edition, cataloging the historical
        gaffes of politicians, or writing short factsheets on topics in the news. Good judgment and writing skills are a
        must!</p>