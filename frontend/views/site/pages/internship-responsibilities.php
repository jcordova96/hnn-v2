<h1 class="invert">Internship Responsibilities</h1>
<h1>Welcome to HNN!</h1>

    <p>The History News Network (HNN) (<a href="//historynewsnetwork.org/">//HNN.us</a>) was created in June 2001 and features
        articles by historians about current events. The website is the brainchild of historians and journalists who are
        fed up with the superficiality of sound bite, what-happens-today-is-all-that-counts journalism.&nbsp; Our aim is
        to give journalists and the public a one-stop-shopping website where current events are constantly being put
        into perspective by historians.&nbsp; HNN is the only website on the Internet wholly devoted to this task. We
        average 300,000 unique visitors and millions of hits a month.</p>

    <p>HNN needs interns to help generate all of our exciting original content.&nbsp; If you want experience in the
        nitty-gritty of Internet journalism, editing, and graphics, our internship program will look great on your
        resume and give you valuable experience.</p>

    <p>The founder, publisher, and editor-in-chief of HNN is Rick Shenkman, a journalist and historian who can regularly
        be seen on MSNBC, Fox News and CNN. He is the author of numerous books, including the best-selling <em>Just How
            Stupid Are We?</em></p>

    <p>HNN has been cited by numerous other publications and websites, including the <em>New York Times</em>, the <em>Los
            Angeles Times</em>, <em>Newsweek/Daily Beast</em>, <em>Salon</em>, <em>Slate</em>, <em>USA Today</em>, and
        many others.</p>

    <p><strong>The Basics</strong></p>

    <p>Internships are not paid positions, but students may be able to apply for class credit. Check with your academic
        advisor.</p>

    <p><em>Hours:</em> Usually 6 hours per week, depending on level of responsibility</p>

    <p><em>Place of Work:</em> Students may do their work at home, the library, a cafe -- anywhere they can access the
        Internet. Students communicate with the editor through email and a Yahoo Group. Students in the Washington, D.C.
        area have the option of working directly with HNN editor David Austin Walsh, though the main HNN office in
        Seattle, WA does not currently have an intern position available.</p>

    <p>All students are welcome to apply, regardless of class level. Enthusiasm is key.</p>

    <p><strong>Basic Responsibilities at HNN.</strong></p>

    <p>No matter what specific jobs you’ve been assigned, every week every intern should do the following:</p>

    <p>#1: Be sure to sign up for the HNN Newsletter and be sure to read it! This is the single best way to
        <strong>A) </strong>understand the kind of content HNN publishes and <strong>B)</strong> keep up to date with
        topics we're reporting on!</p>

    <p>#2: Every Friday you should send in a report reviewing your activities. Use a bullet format and be brief.</p>
<ul>
    <li>Surf the Internet for news about interesting history sites we can tell our readers
        about.
    </li>
    <li>Promote the website by clicking onto discussion groups and chat rooms and news groups
        and letting participants know about articles that might be of interest. Because these groups usually dislike
        pure announcements, be sure to actually say something relevant to the conversation at hand in addition to
        providing a link. Never disguise your identity. As journalists we must be open and honest. If we aren’t, how can
        we demand that others be?
    </li>
    <li>Brainstorm to help the editor develop new ideas for cover stories and articles.</li>
    <li>Be on the look out for interesting writers. Email them if you see a good book review
        or essay and ask if they’d be interested in writing for HNN. We can’t pay anybody, but we can offer them a
        national forum.
    </li>
    <li>Suggest graphics we can use to help enliven the site.</li>
    <li>Dream up ways we can promote the site and sign up new members to our free weekly
        newsletter.
    </li>
    <li>Go through the H-Net listserves and find interesting topics for us to cover. Be on
        the look out for interesting book reviews. (H Net address: <a href="//www2.h-net.msu.edu/lists/">//www2.h-net.msu.edu/lists/</a>)
        Just click on the various discussion groups, dip into the emails and see what topics might be of interest.
    </li>
    </ul>
    <p><b>Specific responsibilities for internships are outlined on<a href="//historynewsnetwork.org/articles/6364.html"> this
                page.</a></b></p>

