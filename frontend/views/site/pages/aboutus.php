    <h3><b>Our Mission&nbsp;</b></h3>

    <p>Welcome to George Mason University's History News Network (which is popularly known as HNN).</p>

    <p>Our mission is to help put current events into historical perspective. Given how public opinion is shaped today,
        whipsawed emotionally on talk shows this way and that in response to the egos of the guests, the desire for
        ratings by the hosts and the search for profits by media companies and sponsors, historians are especially
        needed now. They can help remind us of the superficiality of what-happens-today-is-all-that-counts
        journalism.</p>

    <p>Each week HNN features up to a dozen fresh op eds by prominent historians. Our archives, extending over the past
        decade, include thousands of well-researched pieces.</p>

    <p>Even those who profess utter indifference to history are beholden to it. History is inescapable. Who we are and
        how we react to events depends, to a great extent, on our past. As Eugene O'Neill has a character in <i>Long
            Day's Journey into Night</i> exclaim, at a critical juncture,"The past is the present, isn't it? It's the
        future, too. We all try to lie out of that but life won't let us." Our motto at HNN reflects O'Neill's
        insight:"History News Network ... Because the past is the present, and the future, too."</p>

    <p>Journalism is said to be the first draft of history. But journalists traditionally have had little use for
        historians. The list of occasions on which journalists feel compelled to call upon historians is short. Though a
        select number of historians recently have become media stars, the fact remains that few are publicly quoted, and
        hardly any are given the public platform regularly awarded economists, political scientists or pollsters. The
        last historian trusted to take a large and visible role in a national administration was Arthur Schlesinger,
        Jr., and that was some fifty years ago.</p>

    <p>Among the many duties we assume are these: To expose politicians who misrepresent history. To point out bogus
        analogies. To deflate beguiling myths. To remind Americans of the irony of history. To put events in context. To
        remind us all of the complexity of history.</p>

    <p>Because we believe history is complicated our pages are open to people of all political persuasions. Left, right,
        center: all are welcome.</p>

